window.accion = async (vehiculoId, codigo) => {

  const ref = doc(db, "vehiculos", vehiculoId);
  const snapshot = await getDoc(ref);
  const vehiculo = snapshot.data();

  let estado = vehiculo.estado;
  let nuevaUbicacion = vehiculo.ubicacion;

  const tieneIncidente = vehiculo.incidente_asignado !== null;

  switch (codigo) {

    case "6-3": // En el lugar
      estado = "En Servicio";
      nuevaUbicacion = new GeoPoint(-33.4, -70.6); // luego lo conectamos a emergencia real
      break;

    case "6-7":
      estado = "En Servicio";
      break;

    case "6-8": // Disponible (libera)
      estado = "Disponible";
      if (tieneIncidente) {
        await updateDoc(ref, {
          incidente_asignado: null
        });
      }
      break;

    case "6-9":
      estado = "En Llamado";
      break;

    case "6-10":
      estado = "En base";
      break;

    case "6-11":
      estado = "Fuera de servicio";
      break;

    case "6-13":
      estado = "En Servicio";
      break;

    case "6-14":
      estado = "En Llamado";
      break;

    case "6-15":
      estado = "En Servicio";
      break;
  }

  await updateDoc(ref, {
    estado: estado,
    codigo_estado: codigo,
    ubicacion: nuevaUbicacion
  });
};
