function getColor(estado) {
  if (estado === "Disponible") return "green";
  if (estado === "En Llamado") return "orange";
  if (estado === "En Servicio") return "blue";
  if (estado === "Fuera de servicio") return "gray";
  if (estado === "En base") return "black";
  return "black";
}

